    private void txtFiltroProdutoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltroProdutoKeyReleased
        TableRowSorter rs = new TableRowSorter(otmFiltroProduto);
        rs.setRowFilter(RowFilter.regexFilter("(?i)" + txtFiltroProduto.getText().trim()));
        tpFiltroProduto.setRowSorter(rs);
    }//GEN-LAST:event_txtFiltroProdutoKeyReleased

    private void tpFiltroProdutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tpFiltroProdutoMouseClicked
        if (evt.getClickCount() >= 2) {
            
            Produto produto = null;
            
            if (tpFiltroProduto.getRowSorter() != null) {
                int linhaSelecionada = tpFiltroProduto.getRowSorter().convertRowIndexToModel(tpFiltroProduto.getSelectedRow());
                produto = otmFiltroProduto.getValue(linhaSelecionada);
            } else {
                produto = otmFiltroProduto.getValue(tpFiltroProduto.getSelectedRow());
            }

            if (parent instanceof TelaCompra) {
                TelaCompra tc = (TelaCompra) parent;
                tc.setProduto(produto);
                dispose();
            } else if (parent instanceof TelaVenda) {
                TelaVenda tc = (TelaVenda) parent;
                tc.setProduto(produto);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Campo de destino inv√°lido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            }

        }
    }//GEN-LAST:event_tpFiltroProdutoMouseClicked

